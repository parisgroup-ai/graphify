use std::fmt::Write as FmtWrite;
use std::path::Path;

use graphify_core::graph::CodeGraph;

/// Escapes a string for use inside a Cypher single-quoted string literal.
///
/// Replaces backslashes and single quotes with their escaped forms.
fn cypher_escape(s: &str) -> String {
    s.replace('\\', "\\\\").replace('\'', "\\'")
}

/// Writes a Neo4j Cypher import script to `path`.
///
/// The script creates one node per graph node (labelled by `NodeKind`) and one
/// relationship per edge (typed by `EdgeKind`).  All properties are included as
/// Cypher map literals.
///
/// # Panics
/// Panics if file I/O fails.
pub fn write_cypher(graph: &CodeGraph, path: &Path) {
    let mut buf = String::new();

    writeln!(buf, "// Generated by Graphify — Neo4j Cypher import script").unwrap();
    writeln!(
        buf,
        "// Nodes: {}, Edges: {}",
        graph.node_count(),
        graph.edge_count()
    )
    .unwrap();
    writeln!(buf).unwrap();

    // Nodes
    for node in graph.nodes() {
        let label = format!("{:?}", node.kind);
        let file_path = cypher_escape(&node.file_path.to_string_lossy());
        let language = format!("{:?}", node.language);
        // FEAT-021: emit `alternative_paths` as a Cypher list literal (array
        // of strings). Only written when non-empty so legacy-shape parity with
        // the JSON writer is preserved.
        if node.alternative_paths.is_empty() {
            writeln!(
                buf,
                "CREATE (:{label} {{id: '{id}', file_path: '{file_path}', language: '{language}', line: {line}, is_local: {is_local}}});",
                id = cypher_escape(&node.id),
                line = node.line,
                is_local = node.is_local,
            )
            .unwrap();
        } else {
            let alts = node
                .alternative_paths
                .iter()
                .map(|p| format!("'{}'", cypher_escape(p)))
                .collect::<Vec<_>>()
                .join(", ");
            writeln!(
                buf,
                "CREATE (:{label} {{id: '{id}', file_path: '{file_path}', language: '{language}', line: {line}, is_local: {is_local}, alternative_paths: [{alts}]}});",
                id = cypher_escape(&node.id),
                line = node.line,
                is_local = node.is_local,
            )
            .unwrap();
        }
    }

    writeln!(buf).unwrap();

    // Edges
    for (src, tgt, edge) in graph.edges() {
        let rel_type = format!("{:?}", edge.kind).to_uppercase();
        let conf_kind = format!("{:?}", edge.confidence_kind);
        writeln!(
            buf,
            "MATCH (a {{id: '{src}'}}), (b {{id: '{tgt}'}}) CREATE (a)-[:{rel_type} {{weight: {weight}, line: {line}, confidence: {confidence}, confidence_kind: '{conf_kind}'}}]->(b);",
            src = cypher_escape(src),
            tgt = cypher_escape(tgt),
            weight = edge.weight,
            line = edge.line,
            confidence = edge.confidence,
        )
        .unwrap();
    }

    std::fs::write(path, buf).expect("write Cypher file");
}

